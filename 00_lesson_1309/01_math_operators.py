num_1 = 12
num_2 = 4

print(num_1 + num_2)  # операция сложение
print(num_1 - num_2)  # операция вычитание
print(num_1 * num_2)  # операция умножение
print(num_1 ** num_2)  # операция возведение в степень 2 ** 3 = 8
print(num_1 / num_2)  # операция деление 10 / 3 = 3.3333333
print(num_1 // num_2)  # операция целочисленное деление 10 // 3 = 3
print(num_1 % num_2)  # возврат остатка от деления 10 % 3 = 1
